export class AbsGraph {
    // Operaciones generadoras
    // Crear grafo vacio -> Devuelve un grafo vacio
    // Añadir arista -> Crea una relación entre 2 nodos del grafo
    // Añadir nodo -> Añade el nodo al grafo, sino existía previamente

    // Operaciones Constructores
    // Borrar Nodo -> Elimina un nodo y sus aristas
    // Borrar Arista -> Elimina la arista del nodo si existe

    // Operaciones Selectores
    // Es Vacio -> Bool (Si no tiene nodos)
    // Contiene nodo -> Bool (si contiene el nodo)
    // Adyacentes -> Si dos nodos tienen una arista que los relaciona

    // CASO ESPECIAL DE GRAFOS DIRIGIDOS (Es otra clase) (las aristas tienen direccion)
    // Predecesor nodo1 de nodo2 -> bool (si una arista apunta de nodo1 a nodo2)
    // Sucesor nodo1 de nodo2 -> bool (si una arita apunta de nodo222 a nodo111)
}
